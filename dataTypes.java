
public class dataTypes {
	public static void main(String[] args) {
		byte myByte = 10;
		short myShort = 20;
		int myInt = 30;
		float myFloat = 40.0f;
		double myDouble = 50.0d;
		long myLongInt = 60l;
		char myChar = 'A';
		boolean myBoolean = true;
		
		System.out.println("The Basic Primitive Data Types " +
				"\nByte = "+ myByte +
				"\nShort = "+ myShort +
				"\nInt = "+ myInt +
				"\nFloat = "+ myFloat +
				"\nDouble = "+ myDouble + 
				"\nLongInt = "+ myLongInt + 
				"\nChar = " + myChar + 
				"\nBoolean = "+ myBoolean
				);

	}

}
